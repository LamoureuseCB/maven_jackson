public class User {
    private int id;
    private String name;
    private int age;
    private boolean hasSubscription;

    public User() {
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public boolean isHasSubscription() {
        return hasSubscription;
    }
}
